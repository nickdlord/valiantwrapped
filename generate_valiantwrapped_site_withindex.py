def now_iso() -> str:
    return datetime.now().isoformat(timespec="seconds")

def canonical_author_label(value: str) -> str:
    """
    Convert values like:
      'Kim_Michael_58290603100.csv'
      'author_csvs/Kim_Michael_58290603100.csv'
    into:
      'Kim_Michael_58290603100'
    """
    if value is None or (isinstance(value, float) and pd.isna(value)):
        return ""
    s = str(value).strip()
    s = os.path.basename(s)
    s = s.replace("\\", "/")
    if s.lower().endswith(".csv"):
        s = s[:-4]
    if s.lower().endswith(".txt"):
        s = s[:-4]
    return s.strip()

def parse_author_name(label: str):
    parts = label.split("_")
    last = parts[0] if len(parts) > 0 else ""
    first = parts[1] if len(parts) > 1 else ""
    return first, last

def display_name_from_label(label: str) -> str:
    first, last = parse_author_name(label)
    name = f"{first} {last}".strip()
    return name if name else label

def safe_split_themes(x) -> list[str]:
    """
    Supports themes in formats like:
      "Theme A; Theme B; Theme C"
      "Theme A, Theme B, Theme C"
      ["Theme A","Theme B"] (stringified JSON-ish)
    If you don't have themes, returns [].
    """
    if x is None or (isinstance(x, float) and pd.isna(x)):
        return []
    s = str(x).strip()
    if not s:
        return []
    # try JSON array
    if s.startswith("[") and s.endswith("]"):
        try:
            arr = json.loads(s)
            if isinstance(arr, list):
                return [str(t).strip() for t in arr if str(t).strip()]
        except Exception:
            pass
    # split by common delimiters
    if ";" in s:
        parts = [p.strip() for p in s.split(";")]
    else:
        parts = [p.strip() for p in s.split(",")]
    return [p for p in parts if p]

def format_tracklist(tracklist) -> str:
    if tracklist is None or (isinstance(tracklist, float) and pd.isna(tracklist)):
        return ""

    raw = str(tracklist).strip()
    if not raw:
        return ""

    if "\n" in raw:
        tracks = [t.strip() for t in raw.splitlines() if t.strip()]
    else:
        for sep in [";", "|", ","]:
            if sep in raw:
                tracks = [t.strip() for t in raw.split(sep) if t.strip()]
                break
        else:
            tracks = [raw]

    cleaned = []
    for t in tracks:
        t = re.sub(r"^\s*(track\s*\d+)\s*[-:]\s*", "", t, flags=re.IGNORECASE)
        t = re.sub(r"^\s*\d+\.\s*", "", t)
        cleaned.append(t.strip())

    items = []
    for i, t in enumerate(cleaned, start=1):
        safe = html_lib.escape(t)
        items.append(
            f"""
            <div class="track-row">
                <div class="track-num">{i:02d}</div>
                <div class="track-title">{safe}</div>
            </div>
            """
        )

    return f'<div class="tracklist">\n{"".join(items)}\n</div>'

def find_album_cover_source(author_label: str) -> Path | None:
    """
    Find a cover image for author_label in outputs/album_covers using common extensions.
    """
    exts = [".png", ".jpg", ".jpeg", ".webp"]
    for ext in exts:
        p = ALBUM_COVERS_SRC_DIR / f"{author_label}{ext}"
        if p.exists():
            return p
    return None

def ensure_album_cover_in_docs(author_label: str) -> tuple[bool, str, str]:
    """
    Ensures cover exists in docs/assets/album_covers/ (copies if needed).
    Returns:
      (ok, rel_from_author_page, rel_from_docs_root)

    - rel_from_author_page used on docs/authors/<author>.html
    - rel_from_docs_root used on docs/index.html browse cards
    """
    src = find_album_cover_source(author_label)
    if src is None:
        build_report.append((author_label, "missing_album_cover", f"no file in {ALBUM_COVERS_SRC_DIR}"))
        return False, "", ""

    dst = COVERS_DIR / src.name
    try:
        # Copy only if missing or stale
        if (not dst.exists()) or (dst.stat().st_mtime < src.stat().st_mtime):
            shutil.copy2(src, dst)
    except Exception as e:
        build_report.append((author_label, "album_cover_copy_failed", repr(e)))
        return False, "", ""

    if not dst.exists():
        build_report.append((author_label, "album_cover_copy_missing_dst", str(dst)))
        return False, "", ""

    # From docs/authors/<author>.html to docs/assets/album_covers/<file>
    rel_author = f"../assets/album_covers/{html_lib.escape(dst.name)}"
    # From docs/index.html to docs/assets/album_covers/<file>
    rel_root = f"assets/album_covers/{html_lib.escape(dst.name)}"
    return True, rel_author, rel_root

def album_cover_block(author_label: str, artist_name_raw: str, album_title_raw: str) -> str:
    ok, rel_from_author, _rel_from_root = ensure_album_cover_in_docs(author_label)
    if not ok:
        return """
        <div class="cover-placeholder">
          Album cover art not available yet.
        </div>
        """
    alt = html_lib.escape(f"Album cover for {artist_name_raw} — {album_title_raw}".strip(" —"))
    return f"""
      <div class="cover-wrap">
        <img class="album-cover" src="{rel_from_author}" alt="{alt}" loading="lazy">
      </div>
    """

